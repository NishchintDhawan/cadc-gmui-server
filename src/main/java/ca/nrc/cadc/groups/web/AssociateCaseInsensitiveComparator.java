package ca.nrc.cadc.groups.web;

import java.util.Comparator;


public final class AssociateCaseInsensitiveComparator
        implements Comparator<Associate>
{
    @Override
    public int compare(final Associate associate, final Associate associate2)
    {
        final int idComp =
                associate.getID().compareToIgnoreCase(associate2.getID());
        final int comparison;

        if (idComp == 0)
        {
            // Users come before Groups in the list.
            comparison = Boolean.valueOf(associate2.isUser()).compareTo(
                    associate.isUser());
        }
        else
        {
            comparison = idComp;
        }

        return comparison;
    }
}
